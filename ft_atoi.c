#include "libft.h"

int	ft_atoi(const char *str)
{
	char	sign;
	long	nbr;

	nbr = 0;
	sign = 1;
	while ((*str >= '\t' && *str <= '\r') || *str == ' ')
		str++;
	if (*str == '-' || *str == '+')
		if (*str++ == '-')
			sign = -1;
	while (*str >= '0' && *str <= '9')
	{
		nbr = 10 * nbr + (*str++ - '0');
		if (nbr < 0)
		{
			if (sign < 0)
				return (0);
			return (-1);
		}
	}
	return (nbr * sign);
}
