#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned long int	i;
	char				*dst;

	i = 0;
	if (s == NULL || f == NULL)
		return (NULL);
	dst = (char *)malloc((ft_strlen((char *)s) + 1) * sizeof(char));
	if (dst == NULL)
		return (NULL);
	while (s[i] != '\0')
	{
		dst[i] = f(i, s[i]);
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
